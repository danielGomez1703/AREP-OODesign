   public boolean add (E value){
       if (head != null){
           Node last = head;
           while (last !=null){
              last=last.next;
           }
           last.next = new Node(value);
       }else{
           head= new Node(value);
       }
     return true;
   }
   
   public boolean remove (Object value){
       if (head != null){
           Node last = head;
           Node nextLast= head.next;
           while (nextLast !=null){
               if (nextLast.equals(value)){
                   last.next=nextLast.next;
               }
               last = last.next;
               nextLast= nextLast.next;
           }
           if(head.value.equals(value))
             head = null;
       }else{
           return false;
       }
     return true;
   }
   
   public String toString(){
       String r = "";
       Node last = head;
       while (last !=null){
             r+=" "+last.toString();
           }
       return r;
   }

